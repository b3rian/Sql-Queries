def greetings(name, age):
    """
    Function to greet a user with their name and age.
    
    Parameters:
    name (str): The name of the user.
    age (int): The age of the user.
    
    Returns:
    str: A greeting message.
    """
    print(f"Hello {name}, you are {age} years old!")

greetings('Alice', 25)